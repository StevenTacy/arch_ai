use async_trait::async_trait;

use crate::{error::AppError, models::Message, provider::AiProvider};

/// Demo provider that returns pre-written Taiwan construction law responses.
/// No network calls, no API key. Keyword-matched against the last user message.
pub struct MockProvider;

impl MockProvider {
    pub fn new() -> Self {
        Self
    }
}

#[async_trait]
impl AiProvider for MockProvider {
    async fn chat(&self, messages: Vec<Message>) -> Result<String, AppError> {
        let last = messages
            .iter()
            .rev()
            .find(|m| matches!(m.role, crate::models::Role::User))
            .map(|m| m.content.as_str())
            .unwrap_or("");

        Ok(pick_response(last).to_string())
    }
}

fn pick_response(msg: &str) -> &'static str {
    let m = msg.to_lowercase();

    if m.contains("建蔽率") || m.contains("容積率") || m.contains("bcr") || m.contains("far")
        || m.contains("coverage") || m.contains("floor area")
    {
        BCR_FAR
    } else if m.contains("退縮") || m.contains("setback") || m.contains("51條")
        || m.contains("第51")
    {
        SETBACK
    } else if m.contains("使用分區") || m.contains("zoning") || m.contains("都市計畫")
        || m.contains("urban plan")
    {
        ZONING
    } else if m.contains("消防") || m.contains("防火") || m.contains("fire") {
        FIRE
    } else if m.contains("公寓") || m.contains("大廈") || m.contains("condominium")
        || m.contains("condo")
    {
        CONDO
    } else if m.contains("建築師") || m.contains("architect") {
        ARCHITECT
    } else {
        DEFAULT
    }
}

